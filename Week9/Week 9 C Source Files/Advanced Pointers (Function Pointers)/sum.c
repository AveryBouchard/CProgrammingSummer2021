// **********************************************
// Function:  sum
//
// Description:  Returns the sum of two integers
//
// Parameters:
//
//   num1 - an integer number
//   num2 - another integer number
//
// Returns:  The sum of the two parameters
// ***********************************************

int sum (int num1, int num2)
{
    return (num1 + num2);  /* return the sum */
}